package com.calypsan.listenup.rpcguard.ksp.codegen

import com.calypsan.listenup.rpcguard.ksp.ReturnShape
import com.calypsan.listenup.rpcguard.ksp.RpcMethodModel
import com.calypsan.listenup.rpcguard.ksp.RpcServiceModel
import com.google.devtools.ksp.processing.CodeGenerator
import com.google.devtools.ksp.processing.Dependencies
import com.google.devtools.ksp.symbol.KSType

internal object GuardedClassWriter {
    private const val PACKAGE = "com.calypsan.listenup.server.rpcguard"

    fun write(
        model: RpcServiceModel,
        codeGenerator: CodeGenerator,
    ) {
        val source = render(model)
        codeGenerator
            .createNewFile(
                dependencies = Dependencies(aggregating = false, model.declaration.containingFile!!),
                packageName = PACKAGE,
                fileName = "${model.simpleName}Guarded",
            ).bufferedWriter()
            .use { it.write(source) }
    }

    private fun render(model: RpcServiceModel): String =
        buildString {
            appendLine("// AUTO-GENERATED by RpcGuardSymbolProcessor — do not edit.")
            appendLine("package $PACKAGE")
            appendLine()
            appendLine("import org.slf4j.Logger")
            appendLine("import org.slf4j.LoggerFactory")
            appendLine("import kotlinx.coroutines.CancellationException")
            appendLine("import kotlinx.coroutines.flow.Flow")
            appendLine("import kotlinx.coroutines.flow.catch")
            appendLine("import kotlinx.coroutines.flow.flow")
            appendLine("import com.calypsan.listenup.api.result.AppResult")
            appendLine("import com.calypsan.listenup.api.error.InternalError")
            appendLine("import com.calypsan.listenup.api.streaming.RpcEvent")
            appendLine("import com.calypsan.listenup.server.rpcguard.RpcGuardMetrics")
            appendLine("import com.calypsan.listenup.server.rpcguard.currentCorrelationId")
            appendLine("import com.calypsan.listenup.server.rpcguard.newCorrelationId")
            appendLine("import com.calypsan.listenup.server.rpcguard.withMdc")
            appendLine()
            appendLine("internal class ${model.simpleName}Guarded(")
            appendLine("    private val delegate: ${model.packageName}.${model.simpleName},")
            appendLine("    private val log: Logger = LoggerFactory.getLogger(\"rpc.${model.simpleName}\"),")
            appendLine("    private val metrics: RpcGuardMetrics = RpcGuardMetrics.global,")
            appendLine(") : ${model.packageName}.${model.simpleName} {")
            for (method in model.methods) {
                appendLine()
                renderMethod(model, method)
            }
            appendLine("}")
        }

    private fun StringBuilder.renderMethod(
        service: RpcServiceModel,
        method: RpcMethodModel,
    ) {
        when (val shape = method.returnShape) {
            is ReturnShape.SuspendAppResult -> {
                renderSuspend(method, shape.inner, service)
            }

            is ReturnShape.FlowOfRpcEvent -> {
                renderFlowStub(method, shape.inner)
            }

            is ReturnShape.Invalid -> {
                error("Invalid shape reached writer; should have been filtered by buildServiceModel")
            }
        }
    }

    private fun StringBuilder.renderSuspend(
        method: RpcMethodModel,
        inner: KSType,
        service: RpcServiceModel,
    ) {
        val params = method.parameters.joinToString(", ") { "${it.name}: ${it.type.qualifiedRender()}" }
        val args = method.parameters.joinToString(", ") { it.name }
        appendLine(
            "    override suspend fun ${method.name}($params): com.calypsan.listenup.api.result.AppResult<${inner.qualifiedRender()}> {",
        )
        appendLine("        val cid = currentCorrelationId() ?: newCorrelationId()")
        appendLine("        return try {")
        appendLine(
            """            withMdc("service" to "${service.simpleName}", "method" to "${method.name}", "correlationId" to cid) {""",
        )
        appendLine("                delegate.${method.name}($args)")
        appendLine("            }")
        appendLine("        } catch (e: kotlinx.coroutines.CancellationException) {")
        appendLine("            throw e")
        appendLine("        } catch (e: Throwable) {")
        appendLine(
            "            log.error(\"Uncaught exception in ${service.simpleName}.${method.name} [cid=\$cid]\", e)",
        )
        appendLine(
            """            metrics.recordEscape("${service.simpleName}", "${method.name}", e::class.simpleName ?: "Unknown")""",
        )
        appendLine("            AppResult.Failure(")
        appendLine("            com.calypsan.listenup.api.error.InternalError(")
        appendLine("                correlationId = cid,")
        appendLine("                cause = e::class.simpleName,")
        appendLine("                debugInfo = \"\${e::class.simpleName}: \${e.message}\",")
        appendLine("            )")
        appendLine("            )")
        appendLine("        }")
        appendLine("    }")
    }

    /** Emits a compilable stub for Flow methods. Task 6 replaces this with the real guarded body. */
    private fun StringBuilder.renderFlowStub(
        method: RpcMethodModel,
        inner: KSType,
    ) {
        val params = method.parameters.joinToString(", ") { "${it.name}: ${it.type.qualifiedRender()}" }
        val returnType =
            "kotlinx.coroutines.flow.Flow<" +
                "com.calypsan.listenup.api.streaming.RpcEvent<${inner.qualifiedRender()}>>"
        appendLine("    // TODO Task 6: render guarded Flow path for ${method.name}")
        appendLine("    override fun ${method.name}($params): $returnType =")
        appendLine("        kotlinx.coroutines.flow.flow { }")
    }
}

private fun KSType.qualifiedRender(): String {
    val name = declaration.qualifiedName?.asString() ?: declaration.simpleName.asString()
    return if (arguments.isEmpty()) {
        name
    } else {
        name +
            arguments.joinToString(prefix = "<", postfix = ">") { arg ->
                arg.type?.resolve()?.qualifiedRender() ?: "*"
            }
    }
}
