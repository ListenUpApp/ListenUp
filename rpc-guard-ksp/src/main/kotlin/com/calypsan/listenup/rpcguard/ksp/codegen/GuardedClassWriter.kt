package com.calypsan.listenup.rpcguard.ksp.codegen

import com.calypsan.listenup.rpcguard.ksp.ReturnShape
import com.calypsan.listenup.rpcguard.ksp.RpcMethodModel
import com.calypsan.listenup.rpcguard.ksp.RpcServiceModel
import com.google.devtools.ksp.processing.CodeGenerator
import com.google.devtools.ksp.processing.Dependencies
import com.google.devtools.ksp.symbol.KSType

internal object GuardedClassWriter {
    private const val PACKAGE = "com.calypsan.listenup.server.rpcguard"
    private const val INDENT_CLOSE_PAREN = "            )"

    // File-private C2 session-liveness gate, appended to every streaming guard. Re-checks [isLive]
    // on a ~30s cadence for the life of the stream; the moment it flips false it emits a terminal
    // RpcEvent.Error and cancels upstream, so a revoked session's live stream is severed within the
    // poll window. Inert when [isLive] is null. The Error is emitted BEFORE cancelling upstream:
    // cancelling unblocks the join() below, which would otherwise cancel this gate mid-send.
    private val SESSION_LIVENESS_GATE =
        """
        private const val STREAM_LIVENESS_POLL_MILLIS = 30000L

        private fun <T> Flow<RpcEvent<T>>.gatedByLiveness(
            isLive: (suspend () -> Boolean)?,
            revoked: () -> com.calypsan.listenup.api.error.AppError,
        ): Flow<RpcEvent<T>> {
            if (isLive == null) return this
            val upstream = this
            return channelFlow {
                val upstreamJob = launch { upstream.collect { send(it) } }
                val gateJob =
                    launch {
                        while (true) {
                            kotlinx.coroutines.delay(STREAM_LIVENESS_POLL_MILLIS)
                            if (!isLive()) {
                                send(RpcEvent.Error(revoked()))
                                upstreamJob.cancel()
                                return@launch
                            }
                        }
                    }
                upstreamJob.join()
                gateJob.cancel()
            }
        }

        """.trimIndent() + "\n"

    fun write(
        model: RpcServiceModel,
        codeGenerator: CodeGenerator,
    ) {
        val source = render(model)
        codeGenerator
            .createNewFile(
                dependencies = Dependencies(aggregating = false, model.declaration.containingFile!!),
                packageName = PACKAGE,
                fileName = "${model.simpleName}Guarded",
            ).bufferedWriter()
            .use { it.write(source) }
    }

    private fun render(model: RpcServiceModel): String =
        buildString {
            val hasStreaming = model.methods.any { it.returnShape is ReturnShape.FlowOfRpcEvent }
            appendLine("// AUTO-GENERATED by RpcGuardSymbolProcessor — do not edit.")
            appendLine("package $PACKAGE")
            appendLine()
            appendLine("import kotlinx.coroutines.CancellationException")
            appendLine("import kotlinx.coroutines.flow.Flow")
            appendLine("import kotlinx.coroutines.flow.catch")
            appendLine("import kotlinx.coroutines.flow.emitAll")
            appendLine("import kotlinx.coroutines.flow.flow")
            appendLine("import com.calypsan.listenup.api.result.AppResult")
            appendLine("import com.calypsan.listenup.api.error.InternalError")
            appendLine("import com.calypsan.listenup.api.streaming.RpcEvent")
            appendLine("import com.calypsan.listenup.server.rpcguard.currentCorrelationId")
            appendLine("import com.calypsan.listenup.server.rpcguard.newCorrelationId")
            appendLine("import com.calypsan.listenup.server.rpcguard.withMdc")
            if (hasStreaming) {
                appendLine("import kotlinx.coroutines.flow.channelFlow")
                appendLine("import kotlinx.coroutines.launch")
            }
            appendLine()
            appendLine("class ${model.simpleName}Guarded(")
            appendLine("    private val delegate: ${model.packageName}.${model.simpleName},")
            // A service with a streaming method carries the per-connection session-liveness gate (C2):
            // it re-checks the caller's session on a cadence and severs a revoked stream. Null (the
            // default) leaves every stream inert — the shape a unit test / non-authed mount gets. A
            // plain suspend predicate (not a named type) so nothing crosses onto the client export surface.
            if (hasStreaming) {
                appendLine("    private val sessionLiveness: (suspend () -> Boolean)? = null,")
            }
            appendLine(
                "    private val log: io.github.oshai.kotlinlogging.KLogger = " +
                    "io.github.oshai.kotlinlogging.KotlinLogging.logger(\"rpc.${model.simpleName}\"),",
            )
            appendLine(") : ${model.packageName}.${model.simpleName} {")
            for (method in model.methods) {
                appendLine()
                renderMethod(model, method)
            }
            appendLine("}")
            // The C2 session-liveness gate, emitted file-private per streaming guard. Inlined here
            // rather than shared from :contract commonMain so no server-infra symbol crosses onto the
            // client export surface and so it stays same-module with the generated guard (no
            // cross-module internal-visibility problem). Behaviour is proven by RpcGuardLivenessTest.
            if (hasStreaming) {
                appendLine()
                append(SESSION_LIVENESS_GATE)
            }
        }

    private fun StringBuilder.renderMethod(
        service: RpcServiceModel,
        method: RpcMethodModel,
    ) {
        when (val shape = method.returnShape) {
            is ReturnShape.SuspendAppResult -> {
                renderSuspend(method, shape.inner, service)
            }

            is ReturnShape.FlowOfRpcEvent -> {
                renderFlow(service, method, shape.inner)
            }

            is ReturnShape.Invalid -> {
                error("Invalid shape reached writer; should have been filtered by buildServiceModel")
            }
        }
    }

    private fun StringBuilder.renderSuspend(
        method: RpcMethodModel,
        inner: KSType,
        service: RpcServiceModel,
    ) {
        val params = method.parameters.joinToString(", ") { "${it.name}: ${it.type.qualifiedRender()}" }
        val args = method.parameters.joinToString(", ") { it.name }
        appendLine(
            "    override suspend fun ${method.name}($params): com.calypsan.listenup.api.result.AppResult<${inner.qualifiedRender()}> {",
        )
        appendLine("        val cid = currentCorrelationId() ?: newCorrelationId()")
        appendLine("        return try {")
        appendLine(
            """            withMdc("service" to "${service.simpleName}", "method" to "${method.name}", "correlationId" to cid) {""",
        )
        appendLine("                delegate.${method.name}($args)")
        // Stamp the request's correlation id onto a RETURNED domain failure (the escaped-exception
        // path below already stamps its InternalError) and log a concise, PII-free failure line so
        // the operator's log links to the cid the client surfaces. Same package → no import.
        appendLine(
            """            }.stampAndLogFailure(cid, log, "${service.simpleName}", "${method.name}")""",
        )
        appendLine("        } catch (e: kotlinx.coroutines.CancellationException) {")
        appendLine("            throw e")
        appendLine("        } catch (e: Throwable) {")
        appendLine(
            "            log.error(e) { \"Uncaught exception in ${service.simpleName}.${method.name} [cid=\$cid]\" }",
        )
        // Only the correlation id crosses the wire. The exception's class name + message stay in the
        // server log above (they routinely embed SQL / paths / hostnames); `cause` and `debugInfo`
        // default to null so no server-internal detail is serialized to the client.
        appendLine("            AppResult.Failure(")
        appendLine("            com.calypsan.listenup.api.error.InternalError(")
        appendLine("                correlationId = cid,")
        appendLine(INDENT_CLOSE_PAREN)
        appendLine(INDENT_CLOSE_PAREN)
        appendLine("        }")
        appendLine("    }")
    }

    private fun StringBuilder.renderFlow(
        service: RpcServiceModel,
        method: RpcMethodModel,
        inner: KSType,
    ) {
        val params = method.parameters.joinToString(", ") { "${it.name}: ${it.type.qualifiedRender()}" }
        val args = method.parameters.joinToString(", ") { it.name }
        val innerRendered = inner.qualifiedRender()
        val returnType =
            "kotlinx.coroutines.flow.Flow<" +
                "com.calypsan.listenup.api.streaming.RpcEvent<$innerRendered>>"
        appendLine("    override fun ${method.name}($params): $returnType =")
        // `flow { emitAll(delegate.x()) }` (not `delegate.x().catch {}`): a synchronous throw while
        // CONSTRUCTING the flow — a require/precondition/DI lookup in the non-suspend factory — would
        // escape a bare `.catch` (which never attaches), leaking a raw stacktrace to kotlinx.rpc's
        // default. Wrapping defers construction into the collected flow so the same `.catch` sanitizes it.
        //
        // `.gatedByLiveness(...)` (C2): re-check the caller's session on a ~30s cadence and sever the
        // stream with a terminal RpcEvent.Error the moment it's revoked — a still-valid access JWT must
        // not outlive a revoked session on a live socket. Inert when sessionLiveness is null.
        appendLine("        flow { emitAll(delegate.${method.name}($args)) }")
        appendLine(
            "            .gatedByLiveness(sessionLiveness) " +
                "{ com.calypsan.listenup.api.error.AuthError.SessionExpired() }",
        )
        appendLine("            .catch { e ->")
        appendLine("            if (e is kotlinx.coroutines.CancellationException) throw e")
        appendLine("            val cid = currentCorrelationId() ?: newCorrelationId()")
        appendLine(
            "            log.error(e) { \"Uncaught flow exception in ${service.simpleName}.${method.name} [cid=\$cid]\" }",
        )
        // Only the correlation id crosses the wire. The exception's class name + message stay in the
        // server log above; `cause` and `debugInfo` default to null so no server-internal detail is
        // serialized to the client.
        appendLine("            emit(")
        appendLine("            com.calypsan.listenup.api.streaming.RpcEvent.Error(")
        appendLine("                com.calypsan.listenup.api.error.InternalError(")
        appendLine("                    correlationId = cid,")
        appendLine("                )")
        appendLine(INDENT_CLOSE_PAREN)
        appendLine(INDENT_CLOSE_PAREN)
        appendLine("        }")
    }
}

private fun KSType.qualifiedRender(): String {
    val name = declaration.qualifiedName?.asString() ?: declaration.simpleName.asString()
    val nullable = if (isMarkedNullable) "?" else ""
    return if (arguments.isEmpty()) {
        name + nullable
    } else {
        name +
            arguments.joinToString(prefix = "<", postfix = ">") { arg ->
                arg.type?.resolve()?.qualifiedRender() ?: "*"
            } + nullable
    }
}
