// GraalVM native-image build for the server. Exploratory — see
// docs/superpowers/findings/2026-06-09-graalvm-native-image-feasibility.md. Driven by
// GRAALVM_HOME; run native tasks with `--no-configuration-cache` (plugin 0.11.0 limitation).
graalvmNative {
    // The Gradle daemon is pinned to a JetBrains-vendor JDK (gradle-daemon-jvm.properties), so
    // toolchain selection would hand native-image a non-GraalVM JDK. Resolve native-image from
    // GRAALVM_HOME instead.
    toolchainDetection.set(false)
    // Pull upstream reachability metadata for our dependencies (java-jwt, logback, exposed,
    // hikari, flyway, …) automatically.
    metadataRepository {
        enabled.set(true)
    }
    // `./gradlew :server:test -Pagent` captures runtime metadata; `:server:metadataCopy` then
    // promotes it into src/main/resources/META-INF/native-image where native-image auto-discovers
    // it — the committed, reviewed artifact. Metadata coverage == test coverage.
    agent {
        metadataCopy {
            inputTaskNames.add("test")
            outputDirectories.add("src/main/resources/META-INF/native-image/com.calypsan.listenup/server")
            mergeWithExisting.set(false)
        }
    }
    binaries.named("main") {
        imageName.set("listenup-server")
        mainClass.set("com.calypsan.listenup.server.LauncherKt")
        // NOTE: requires GraalVM for JDK 21.0.11+ (substrate 23.1-b92+). Older builds (e.g. CE
        // 21.0.2 / 23.1-b30) crash analysing Kotlin suspend lambdas that rethrow CancellationException
        // ("Frame states being merged are incompatible: mismatch in rethrowException flag", #647).
        buildArgs.add("-H:+ReportExceptionStackTraces")
        // Bound the heap so RSS is predictable on RAM-constrained NAS/mini-PC targets (native-image
        // otherwise sizes max heap to a big fraction of host RAM). 512m leaves headroom for Argon2's
        // 64MB-per-hash overlapping a library scan — 128m/256m OOM that combo. Measured: ~211MB idle
        // for the auth-only path, bounded well below the equivalent JVM build (~405/508MB). Tunable
        // at runtime with -Xmx. (#647)
        buildArgs.add("-R:MaxHeapSize=512m")
        // Canonical Kotlin-on-native-image fix for the annotation/deprecation enums that get
        // initialized during image build.
        // Minimal build-time-init: only the specific Kotlin stdlib classes native-image flags
        // (annotation/deprecation enums + the lazy-delegate impl behind generated serializers).
        // Broad build-time-init (whole `kotlin`/contract packages) is deliberately avoided — it
        // creates DynamicHubs that retain the system classloader's open JarFile handles in the
        // image heap. The sealed-serializer subtype companions are made reachable by the committed
        // reflect-config (captured by ContractSerializerRegistrationTest), not by build-time-init.
        val buildTimeInit =
            listOf(
                "kotlin.annotation.AnnotationRetention",
                "kotlin.annotation.AnnotationTarget",
                "kotlin.DeprecationLevel",
                "kotlin.SafePublicationLazyImpl",
            )
        buildArgs.add("--initialize-at-build-time=${buildTimeInit.joinToString(",")}")
        // Logback + SLF4J initialize at BUILD time — the canonical native-image setup (matches the
        // reachability-metadata repo's expectation). A half-initialized logback (some classes
        // build-time, some run-time) is what retains the system classloader's open JarFile handles
        // in the heap; initializing the whole slf4j+logback graph at build time resolves it.
        // NOTE: this bakes the logback config at build time, so the runtime LISTENUP_LOG_FORMAT
        // switching (#436 Launcher) needs a native-specific rethink later — tracked as follow-up.
        buildArgs.add("--initialize-at-build-time=org.slf4j,ch.qos.logback,net.logstash.logback")
        // #647 fix: GraalVM force-initializes java.time.zone.ZoneRulesProvider at build time; its
        // ServiceLoader scan opens JDK module jars whose handles get retained in the image heap via
        // BuiltinClassLoader.moduleToReader ("Detected a ZipFile object in the image heap"). It is
        // too late to move ZoneRulesProvider to run-time init, so empty that cache at build time.
        buildArgs.add("--features=com.calypsan.listenup.server.nativeimage.ModuleReaderResetFeature")
        // Flyway (and other startup code) resolves https/http URLs; native-image gates URL
        // protocols off by default.
        buildArgs.add("--enable-url-protocols=https,http")
    }
}
