tasks.test {
    useJUnitPlatform()
    // The native-image tracing agent (`-Pagent`) ships only inside GraalVM; the project's
    // pinned JetBrains daemon JDK lacks it. When GRAALVM_HOME is set, run the test JVM on
    // GraalVM directly so the agent can attach and capture reflection/resource/serialization
    // metadata from the real test suite (the agent-over-tests strategy).
    System.getenv("GRAALVM_HOME")?.let { executable = "$it/bin/java" }
    // Recycle the test JVM every 25 classes. The Ktor `testApplication` specs extract the
    // runtime classpath into the working dir on first use; left in a single long-lived worker
    // alongside the full spec count this intermittently corrupts class loading of synthetic
    // lambda inner classes (`<Spec>$1$1` NoClassDefFoundError) and eventually wedges the worker.
    // Periodic forking isolates each batch's classpath extraction and keeps the suite green.
    setForkEvery(25)
    // Redirect the working directory so any Ktor testApplication classpath-extraction
    // artefacts (META-INF/, io/) land under build/ rather than the project root.
    workingDir =
        layout.buildDirectory
            .dir("test-cwd")
            .get()
            .asFile
    // Redirect the test JVM's temp directory under build/ as well. Specs create temp SQLite
    // DBs (`listenup-test-*.db` + SQLite's `-wal`/`-shm` sidecars) and temp dirs via
    // `Files.createTempFile`/`createTempDirectory`; `deleteOnExit()` never covers the sidecars
    // and doesn't fire when a forked worker is killed, so these leaked into the system temp
    // dir (`/var/folders`, which macOS reports as "System Storage") — tens of GB over a few
    // days. Pointing `java.io.tmpdir` here keeps every temp artefact inside build/, so `clean`
    // wipes it and it never touches System Storage.
    val testTmpDir =
        layout.buildDirectory
            .dir("test-tmp")
            .get()
            .asFile
    systemProperty("java.io.tmpdir", testTmpDir.absolutePath)
    // Wipe and recreate before each run: starts every run from empty (bounding disk use) and
    // reclaims leftovers from any previous run whose workers were force-killed.
    doFirst {
        workingDir.mkdirs()
        testTmpDir.deleteRecursively()
        testTmpDir.mkdirs()
    }
}

// ── Native-image migration-index drift gate (#647) ──────────────────────────────────────────────
// Under native-image Flyway can't scan the classpath, so it loads migrations by name from the
// committed `db/migration-index.txt` (see BundledMigrationResourceProvider). A migration added
// without updating the index would silently be missing at native runtime → "no such table". These
// tasks regenerate the index and fail the build (via `check`) if it drifts from the actual SQL files.
val migrationDir = layout.projectDirectory.dir("src/main/resources/db/migration")
val migrationIndexFile = layout.projectDirectory.file("src/main/resources/db/migration-index.txt")

fun actualMigrationNames(): List<String> =
    (migrationDir.asFile.listFiles { f -> f.isFile && f.name.endsWith(".sql") } ?: emptyArray())
        .map { it.name }
        .sorted()

tasks.register("generateMigrationIndex") {
    group = "native"
    description = "Regenerates db/migration-index.txt from the actual migration SQL files."
    doLast {
        migrationIndexFile.asFile.writeText(actualMigrationNames().joinToString("\n", postfix = "\n"))
        logger.lifecycle("Wrote ${actualMigrationNames().size} migration names to ${migrationIndexFile.asFile.name}")
    }
}

val checkMigrationIndex =
    tasks.register("checkMigrationIndex") {
        group = "verification"
        description = "Fails if db/migration-index.txt is out of sync with the migration SQL files."
        doLast {
            val committed = migrationIndexFile.asFile.readLines().map { it.trim() }.filter { it.isNotEmpty() }.toSortedSet()
            val actual = actualMigrationNames().toSortedSet()
            require(committed == actual) {
                "db/migration-index.txt is out of sync with src/main/resources/db/migration — the native " +
                    "server would be missing migrations. Run `./gradlew :server:generateMigrationIndex`.\n" +
                    "  missing from index: ${actual - committed}\n  stale in index:    ${committed - actual}"
            }
        }
    }

tasks.named("check") { dependsOn(checkMigrationIndex) }

